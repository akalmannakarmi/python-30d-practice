# Make the following using string formatting methods:
print("{} + {} = {}".format(8,6,8+6))
print("{} - {} = {}".format(8,6,8-6))
print("{} * {} = {}".format(8,6,8*6))
print("{} / {} = {:.2f}".format(8,6,8/6))
print("{} % {} = {}".format(8,6,8%6))
print("{} // {} = {}".format(8,6,8//6))
print("{} ** {} = {}".format(8,6,8**6))